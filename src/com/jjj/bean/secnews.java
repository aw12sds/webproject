package com.jjj.bean;

public class secnews {
private int id;
private String newscap;
private String newcaption;
private String newssource;
private String data;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNewscap() {
	return newscap;
}
public void setNewscap(String newscap) {
	this.newscap = newscap;
}
public String getNewcaption() {
	return newcaption;
}
public void setNewcaption(String newcaption) {
	this.newcaption = newcaption;
}
public String getNewssource() {
	return newssource;
}
public void setNewssource(String newssource) {
	this.newssource = newssource;
}
public String getData() {
	return data;
}
public void setData(String data) {
	this.data = data;
}

}
